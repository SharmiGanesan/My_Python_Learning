#Return Function
def listfunction():
    return [1,2,3]
print(listfunction())
def tuplefunction():
    return (1,2,3)
print(tuplefunction())
def dictfunction():
    return {1,2,3}
print(dictfunction())
def strfunction():
    return "Ilan"
print(strfunction())
def intfunction():
    return 123
print(intfunction())
def floatfunction():
    return 3.12
print(floatfunction())
def boolfunction():
    return True
print(boolfunction())
def setfunction():
    return[(1,2,3)]
print(setfunction())